import React from 'react'
import ReactDOM from 'react-dom'
import {
  BrowserRouter as Router,
  Route,
  Switch,
  Redirect,
} from 'react-router-dom'

import './style.css'
import Home from './views/home'
import EventsCalendar from './views/events-calendar'
import BlogArticlesDiscourses from './views/blog-articles-discourses'
import NotFound from './views/not-found'

const App = () => {
  return (
    <Router>
      <Switch>
        <Route component={Home} exact path="/" />
        <Route component={EventsCalendar} exact path="/events-calendar" />
        <Route
          component={BlogArticlesDiscourses}
          exact
          path="/blog-articles-discourses"
        />
        <Route component={NotFound} path="**" />
        <Redirect to="**" />
      </Switch>
    </Router>
  )
}

ReactDOM.render(<App />, document.getElementById('app'))
